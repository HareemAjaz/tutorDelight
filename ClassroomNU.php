function updateProgressBar()
{
	var vid = document.getElementById("myVideo");

    vid.ontimeupdate = function() {myFunction()};
   // var seek = seektime;
  var myArray=document.getElementById("seek").value.split(':');
		
		var hr = (myArray[0]*60*60);
	    var min = (myArray[1]*60);
	    var sec = (myArray[2]*1);
	    var sum = hr+min+sec;
	    if(sum > vid.duration)
	    {
		   alert("Error:can not seek out of bound")
	    }
	    else
	    {
		   var currentTime = sum;
		   vid.currentTime = currentTime;
	    }
		Array.clear(myArray);
}
